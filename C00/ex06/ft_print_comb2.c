#include <unistd.h>

void	num(char car)
{
	write(1, &car, 1);
}

void	ft_print_comb2(void)
{
	int	n;
	int	n1;

	n = 0;
	n1 = 0;
	while (n < 100)
	{
		n1 = n + 1;
		while (n1 < 100)
		{
			num(n / 10 + '0');
			num(n % 10 + '0');
			num(' ');
			num(n1 / 10 + '0');
			num(n1 % 10 + '0');
			if (n != 98 || n1 != 99)
			{
				write(1, ", ", 2);
			}
			n1++;
		}
		n++;
	}
}

